a[1][2] = 5  # изменит только 1 эелемент [[0, 0, 0], [0, 0, 5], [0, 0, 0], [0, 0, 0], [0, 0, 0]]

a = [(i, j) for i in 'ab' for j in [1, 2]]  # [('a', 1), ('a', 2), ('b', 1), ('b', 2)]
# видим что соединялось a с 1 потом с 2 и b с 1 потом с 2 (для i и j)
a = [i * j for i in [1, 3, 5] for j in [3, 7, 8] if i * j > 10]  # [21, 24, 15, 35, 40]
# запись в а элементов которые при i*j были больше 10 (1*3,1*7,1*8, 3*3,{<10} 3*7,3*8 и т.д.)
